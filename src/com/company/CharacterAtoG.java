package com.company;

public class CharacterAtoG extends Banner {

    public CharacterAtoG(char character) {
        this.character = character;

        switch (character) {
            case 'A':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'B':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'C':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'D':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'E':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'F':
                line[0] = "*****";
                line[1] = "*    ";
                line[2] = "**** ";
                line[3] = "*    ";
                line[4] = "*    ";
                break;
            case 'G':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
        }
    }


}
