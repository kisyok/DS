package com.company;

public class CharacterHtoN extends Banner {

    public CharacterHtoN(char character) {
        this.character = character;

        switch (character) {
            case 'H':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'I':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'J':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'K':
                line[0] = "*   *";
                line[1] = "* *  ";
                line[2] = "*    ";
                line[3] = "* *  ";
                line[4] = "*   *";
                break;
            case 'L':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
            case 'M':
                line[0] = "*   *";
                line[1] = "** **";
                line[2] = "* * *";
                line[3] = "*   *";
                line[4] = "*   *";
                break;
            case 'N':
                line[0] = "";
                line[1] = "";
                line[2] = "";
                line[3] = "";
                line[4] = "";
                break;
        }
    }
}
